class User:
    def __init__(self, id, name, surname, mail, password, age, eduHistory, gender, country, role_id):
        self.id = id
        self.name = name
        self.surname = surname
        self.mail = mail
        self.password = password
        self.age = age
        self.eduHistory = eduHistory
        self.gender = gender
        self.country = country
        self.role_id = role_id
